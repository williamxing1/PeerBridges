export type TranslationKey = keyof typeof translations.en;

const optionLabels: Record<string, string> = {
  "Elementary school": "小学",
  "Middle school": "初中",
  "High school": "高中",
};

function interpolate(value: string, vars?: Vars) {
  if (!vars) return value;
  return value.replace(/\{(\w+)\}/g, (_, key: string) => String(vars[key] ?? ""));
}

export function translate(lang: Lang, key: TranslationKey, vars?: Vars) {
  return interpolate(translations[lang][key] ?? translations.en[key], vars);
}

export function optionLabel(value: string, lang: Lang) {
  if (lang === "en") {
    return value.length > 0 ? value[0].toUpperCase() + value.slice(1) : value;
  }
  if (value.startsWith("Grade ")) {
    return `${value.match(/\d+/)?.[0] ?? value}年级`;
  }
  return optionLabels[value] ?? value;
}

type LanguageContextValue = {
  lang: Lang;
  setLang: (lang: Lang) => void;
  t: (key: TranslationKey, vars?: Vars) => string;
};

const LanguageContext = createContext<LanguageContextValue | null>(null);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");

  const setLang = useCallback((nextLang: Lang) => {
    setLangState(nextLang);
    localStorage.setItem(languageStorageKey, nextLang);
    document.documentElement.lang = nextLang === "zh" ? "zh-CN" : "en";
  }, []);

  useEffect(() => {
    const savedLanguage = localStorage.getItem(languageStorageKey);
    if (savedLanguage === "en" || savedLanguage === "zh") {
      setLangState(savedLanguage);
      document.documentElement.lang = savedLanguage === "zh" ? "zh-CN" : "en";
    }

    function syncLanguage(event: StorageEvent) {
      if (event.key !== languageStorageKey || (event.newValue !== "en" && event.newValue !== "zh")) return;
      setLangState(event.newValue);
      document.documentElement.lang = event.newValue === "zh" ? "zh-CN" : "en";
    }

    window.addEventListener("storage", syncLanguage);
    return () => window.removeEventListener("storage", syncLanguage);
  }, []);

  const value = useMemo(
    () => ({
      lang,
      setLang,
      t: (key: TranslationKey, vars?: Vars) => translate(lang, key, vars),
    }),
    [lang]
  );

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error("useLanguage must be used inside LanguageProvider");
  }
  return context;
}

