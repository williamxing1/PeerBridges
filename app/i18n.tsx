export type TranslationKey = keyof typeof translations.en;

const optionLabels: Record<string, string> = {
  Beginner: "初级",
  Intermediate: "中级",
  Advanced: "高级",
  "Elementary school": "小学",
  "Middle school": "初中",
  "High school": "高中",
  "elementary school": "小学",
  "middle school": "初中",
  "high school": "高中",
};

function interpolate(value: string, vars?: Vars) {
  if (!vars) return value;
  return value.replace(/\{(\w+)\}/g, (_, key: string) => String(vars[key] ?? ""));
}

export function translate(lang: Lang, key: TranslationKey, vars?: Vars) {
  return interpolate(translations[lang][key] ?? translations.en[key], vars);
}

export function optionLabel(value: string, lang: Lang) {
  if (lang === "en") {
    return value.length > 0 ? value[0].toUpperCase() + value.slice(1) : value;
  }
  if (value.startsWith("Grade ")) {
    return `${value.match(/\d+/)?.[0] ?? value}年级`;
  }
  return optionLabels[value] ?? value;
}

type LanguageContextValue = {
  lang: Lang;
  setLang: (lang: Lang) => void;
  t: (key: TranslationKey, vars?: Vars) => string;
};

const LanguageContext = createContext<LanguageContextValue | null>(null);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>("en");
  const value = useMemo(
    () => ({
      lang,
      setLang,
      t: (key: TranslationKey, vars?: Vars) => translate(lang, key, vars),
    }),
    [lang]
  );

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error("useLanguage must be used inside LanguageProvider");
  }
  return context;
}

export function LanguageSelect() {
  const { lang, setLang } = useLanguage();

  return (
    <Select.Root value={lang} onValueChange={(value) => setLang(value as Lang)}>
      <Select.Trigger className="flex items-center gap-1.5 rounded-lg border border-border bg-background px-2.5 py-1.5 text-sm text-muted-foreground outline-none transition-colors hover:bg-accent hover:text-card-foreground sm:px-3">
        <Select.Value />
        <Select.Icon>
          <ChevronDown size={14} />
        </Select.Icon>
      </Select.Trigger>
      <Select.Portal>
        <Select.Content className="z-50 overflow-hidden rounded-xl border border-border bg-popover shadow-xl">
          <Select.Viewport className="p-1">
            {LANGUAGES.map((language) => (
              <Select.Item
                key={language.code}
                value={language.code}
                className="cursor-pointer rounded-lg px-3 py-2 text-sm text-popover-foreground outline-none data-[highlighted]:bg-accent"
              >
                <Select.ItemText>{language.label}</Select.ItemText>
              </Select.Item>
            ))}
          </Select.Viewport>
        </Select.Content>
      </Select.Portal>
    </Select.Root>
  );
}
